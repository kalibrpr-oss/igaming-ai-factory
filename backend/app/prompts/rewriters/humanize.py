"""
Второй проход (rewrite): убрать остатки шаблонности, сохранить факты и смысл.
"""

from app.prompts.generators.russian_hints import russian_editor_hints_block

REWRITE_PASS_INSTRUCTIONS = """
================================
ЭТАП REWRITE (ОБЯЗАТЕЛЬНЫЙ ВТОРОЙ ПРОХОД)
================================

Ты получаешь ЧЕРНОВИК статьи, уже написанный в выбранном Brand Voice.
Твоя задача — ДОВЕСТИ текст до уровня «написал живой автор», не меняя смысл и факты.

Жёстко:
- сохрани бренд, цифры, названия игр, условия бонусов, сроки — как в черновике; не добавляй новых фактов
- не ломай соответствие ТЗ: объём в словах, обязательные блоки и структура заголовков — сохраняй; если в пользовательском сообщении указан коридор слов — ВЫПОЛНИ его (при переборе сократи текст, выкинув воду, не выкидывая факты и H-структуру)
- если в черновике ошибка или противоречие — не исправляй факты без явного указания в ТЗ (только стилистика)
- не нейтрализуй Brand Voice: если заказ был в aggressive — финал остаётся дерзким и рубленым, не превращай в сухой whitepaper

Сделай:
- убери остатки шаблонных вступлений и AI-ритма
- разнообразь начала предложений и длину фраз
- убери повторы одних и тех же конструкций
- сохрани логику абзацев и связность темы
- выход — только финальный текст статьи, без комментариев и без преамбулы «вот исправленный текст»
""".strip()


def build_rewrite_user_message(
    draft_text: str,
    *,
    target_word_count: int,
    language: str,
    voice_id: str,
) -> str:
    """Пользовательский промпт для шага rewrite_text."""
    tw = target_word_count
    lo = max(300, int(tw * 0.92))
    hi = int(tw * 1.08)

    voice_line = ""
    if voice_id == "bold_daring":
        voice_line = (
            "Сохрани и усиль aggressive-голос: короткие удары, ирония, прямые оценки — не сглаживай под эксперта.\n"
        )

    ru = ""
    if (language or "").lower().startswith("ru"):
        ru = russian_editor_hints_block() + "\n\n"

    return f"""Черновик для доработки. Сохрани факты, цифры и смысл.
Верни только финальный текст статьи (и строку Meta:, если она была в черновике — в конце отдельной строкой).

ОБЪЁМ: целевой {tw} слов по основному тексту (всё до строки «Meta:», её не считать).
Допустимый коридор: {lo}–{hi} слов. Если черновик длиннее {hi} слов — ОБЯЗАТЕЛЬНО ужми: убери воду и повторы, структуру H2/H3 и FAQ сохрани.
{voice_line}{ru}
--- Черновик ---

{draft_text.strip()}
""".strip()
