"""
Глобальные ANTI-AI правила для всех LLM-вызовов с Brand Voice.
"""

ANTI_AI_RULES_BLOCK = """
================================
ANTI-AI ПРАВИЛА (КРИТИЧНО)
================================

Главное: текст должен выглядеть как написанный человеком.
Никакой стерильности, шаблонности и «идеальной гладкости».

--------------------------------
1. ЗАПРЕТ НА КЛИШЕ И ВВОДНЫЕ
--------------------------------

Полностью запрещены фразы:
в современном мире
важно отметить
следует отметить
стоит отметить
данная статья
в данной статье
мы рассмотрим
рассмотрим подробнее
давайте разберёмся
итак
подводя итог
в заключение
как известно
несомненно
безусловно
одним из ключевых факторов является
играет важную роль
имеет значение
широкий спектр возможностей

--------------------------------
2. ЗАПРЕТ НА СТЕРИЛЬНУЮ СТРУКТУРУ
--------------------------------

Нельзя:
- одинаковая длина предложений подряд
- одинаковые конструкции в абзацах
- идеальная логическая «учебниковая» подача
- слишком ровный ритм текста

Нужно:
- перемешивать короткие и длинные предложения
- ломать предсказуемость

--------------------------------
3. ЗАПРЕТ НА «ИДЕАЛЬНЫЙ ЯЗЫК»
--------------------------------

Нельзя:
- писать слишком правильно и гладко
- избегать любой «живости»

Нужно:
- допускать лёгкую разговорность (в рамках выбранного голоса; у Expert — без сленга)
- местами упрощать фразы
- местами делать чуть резче

--------------------------------
4. ЗАПРЕТ НА АБСТРАКЦИИ
--------------------------------

Нельзя:
- «высокие шансы на выигрыш»
- «быстрые выплаты»
- «интересный игровой процесс»

Нужно конкретика. Примеры:
выплата за 5–15 минут
20–50 спинов до бонуса
частые мелкие выплаты

--------------------------------
5. ЗАПРЕТ НА ПОВТОРЫ ПАТТЕРНОВ
--------------------------------

Нельзя:
- начинать предложения одинаково
- повторять одну и ту же структуру

Пример плохого:
Слот даёт бонус.
Слот даёт выплаты.
Слот подходит для игры.

--------------------------------
6. ЗАПРЕТ НА AI-СИММЕТРИЮ
--------------------------------

Нельзя:
- «идеально сбалансированные» абзацы
- одинаковое количество строк
- зеркальная структура

--------------------------------
7. ЗАПРЕТ НА ПЕРЕГРУЗ ЛОГИКОЙ
--------------------------------

Нельзя:
- делать текст как учебник
- перегружать объяснениями

Нужно:
- чередовать факты и простые выводы

--------------------------------
8. ЗАПРЕТ НА СИМВОЛЫ И МАРКЕРЫ AI
--------------------------------

Нельзя использовать в тексте (ни в каком виде как «украшение»):
—
…
!!!
???
::
##

Избегать: слишком много запятых подряд; перегруженные конструкции.

--------------------------------
9. ЗАПРЕТ НА НЕЙТРАЛЬНОСТЬ
--------------------------------

Нельзя:
- писать без позиции
- звучать как «никто»

Нужно:
- лёгкое мнение
- ощущение опыта

--------------------------------
10. ОБЯЗАТЕЛЬНЫЕ ПРИЁМЫ
--------------------------------

Всегда добавлять:
- разная длина предложений
- микро-мнения: «если по факту», «на практике», «чаще всего»
- конкретные игровые ситуации
- лёгкая «неровность»

--------------------------------
11. ВНУТРЕННЯЯ ПРОВЕРКА (ОБЯЗАТЕЛЬНО)
--------------------------------

Перед выдачей текста:
1. Убрать все клише
2. Убрать симметрию
3. Проверить ритм
4. Добавить живость
5. Убедиться, что текст не звучит как AI

Если есть хоть малейшее ощущение «машинности» — переписать.
""".strip()
