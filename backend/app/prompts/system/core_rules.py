CORE_PRODUCTION_RULES_BLOCK = """
================================
КАЧЕСТВО ТЕКСТА (ПРОДАКШН)
================================

Цель: тексты неотличимы от человеческих, без повторяемости и AI-паттернов,
при строгом соблюдении Brand Voice и сохранении смысла.

--------------------------------
1. НИКАКИХ ПОВТОРОВ
--------------------------------

- не повторять одни и те же фразы и обороты
- не копировать структуру абзацев из типовых SEO-шаблонов
- не начинать подряд несколько предложений с одной и той же грамматической конструкции
- каждый новый абзац — с вариацией ритма относительно предыдущего

--------------------------------
2. СМЫСЛ ВСЕГДА ЧЁТКИЙ
--------------------------------

- никакой воды и общих слов без привязки к бренду, слоту, условиям
- никакой абстрактной «болтовни» про индустрию без фактов
- каждое предложение должно нести информационную нагрузку или логичный переход

--------------------------------
3. СВЯЗНОСТЬ
--------------------------------

- текст читается как единый поток, без скачков темы
- предложения логически связаны (причина-следствие, пример-вывод)
- если меняешь подтему — явная смысловая склейка, не телеграфный список несвязных тезисов

--------------------------------
4. АНТИ-ШАБЛОН (РИТМ)
--------------------------------

- избегать «идеальной» симметрии блоков и одинаковой длины абзацев
- чередовать факты, наблюдения и короткие выводы
- (детальные запреты на AI-ритм и клише — в блоке Anti-AI ниже)

--------------------------------
5. ВАРИАТИВНОСТЬ
--------------------------------

- даже при схожем ТЗ каждый текст должен отличаться ритмом, порядком мыслей и формулировками
- избегать копирования собственных предыдущих формулировок из других генераций (если в контексте есть пример — не копируй его структуру дословно)

--------------------------------
6. СООТВЕТСТВИЕ ЗАКАЗУ
--------------------------------

Объём, полнота ТЗ, ключи и обязательные блоки — см. отдельный блок «ТЗ И ОБЪЁМ» в system prompt (идёт сразу после этого блока).
""".strip()


CLIENT_ORDER_RULES_BLOCK = """
================================
ТЗ И ОБЪЁМ (ОБЯЗАТЕЛЬНО)
================================

- Строго выполнять требования клиента из входного ТЗ: язык, бренд, ключи, LSI, тип статьи, параметры SEO, структуру, заметки к заказу и формат вывода.
- Не игнорировать ни один явный пункт ТЗ.
- При конфликте между Brand Voice и ТЗ клиента приоритет всегда у ТЗ клиента.
- Если задан объём в словах — держаться в рамках заданного диапазона; если задан только верхний лимит — не превышать его.
- Если задан объём в символах — строго соблюдать объём в символах.
- Если заданы и слова, и символы — соблюдать оба ограничения, приоритет у более жёсткого ограничения.
- Структура заголовков и обязательных блоков (H1, H2/H3, FAQ, таблица и т.д.) — строго по ТЗ.
- Rewrite и humanization не должны ломать объём, структуру, SEO-требования и формат вывода.
- Не выдумывать цифры, бонусы, лицензии, сроки и факты, которых нет во входных данных; при отсутствии факта — писать нейтрально, без вымысла.
""".strip()


ENERGY_TONE_RULES_BLOCK = """
================================
ENERGY & TONE (АНТИ-УНЫНИЕ)
================================

- Текст не должен создавать ощущение уныния, безысходности или «слива».
- Избегать акцента на проигрышах, тильте, боли и негативных эмоциях.
- Не использовать формулировки, которые демотивируют или отталкивают читателя.
- Допускается упоминание рисков, но в нейтральной или спокойной форме без давления.
- Общий вайб текста — живой, энергичный, с ощущением контроля, а не потери.
- Текст должен вызывать интерес, а не ощущение «лучше не играть».
- Не превращать текст в морализаторство или предупреждение.

--------------------------------
БАЛАНС С ТЗ (КРИТИЧНО)
--------------------------------

- Не искажать факты ради «позитива».
- Если в ТЗ требуется описание рисков — писать их спокойно, без драматизации.
- Приоритет всегда у ТЗ клиента, но подача должна оставаться живой и не унылой.
""".strip()
