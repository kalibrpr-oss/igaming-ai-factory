"""
Второй проход (rewrite): убрать остатки шаблонности, сохранить факты и смысл.
"""

REWRITE_PASS_INSTRUCTIONS = """
================================
ЭТАП REWRITE (ОБЯЗАТЕЛЬНЫЙ ВТОРОЙ ПРОХОД)
================================

Ты получаешь ЧЕРНОВИК статьи, уже написанный в выбранном Brand Voice.
Твоя задача — ДОВЕСТИ текст до уровня «написал живой автор», не меняя смысл и факты.

Жёстко:
- сохрани бренд, цифры, названия игр, условия бонусов, сроки — как в черновике; не добавляй новых фактов
- не ломай соответствие ТЗ: объём в словах, обязательные блоки и структура заголовков — сохраняй, если они уже соблюдены в черновике
- если в черновике ошибка или противоречие — не исправляй факты без явного указания в ТЗ (только стилистика)

Сделай:
- убери остатки шаблонных вступлений и AI-ритма
- разнообразь начала предложений и длину фраз
- убери повторы одних и тех же конструкций
- сохрани логику абзацев и связность темы
- выход — только финальный текст статьи, без комментариев и без преамбулы «вот исправленный текст»
""".strip()


def build_rewrite_user_message(draft_text: str) -> str:
    """Пользовательский промпт для шага rewrite_text."""
    return (
        "Черновик для доработки. Сохрани факты, цифры и смысл. "
        "Верни только финальный текст статьи (и строку Meta:, если она была в черновике).\n\n"
        f"{draft_text.strip()}"
    )
